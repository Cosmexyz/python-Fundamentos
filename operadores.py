print('a soma é: {} \na subtra é: {} \na divisao é: {} \na multi é: {} \na potencia é: {} \no resto de divisao é: {} \na dvisao interira é: {} \na raizQ é: {}: '.format(soma, subtra, divisao, multi, potencia, restoDiv, divInt, raizQ**(1/2)))
