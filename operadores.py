n1 = int(input('digite o primero numero '))
n2 = int(input('digite o segundo numero '))
soma = n1 + n2 #somar
subtra = n1 - n2 #subtrair
divisao = n1 / n2 #divisão
multi = n1 * n2 #multiplicação
